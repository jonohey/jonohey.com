            <h3>What is a Mindmap?</h3>
          </div>
          <div class="span6">
            <p>A Mindmap is an easy way to get information out of your head and on paper. Typically you start at the centre with a topic idea and then branch out as you build associations from it. Mindmaps are non-linear (not word-by-word) and so they can be taken in more easily and browsed quickly. We tried to keep them visual to trigger memories faster and draw a reader into the page. I learned about Mindmaps first from <a href="http://www.amazon.com/s/ref=nb_sb_noss?url=search-alias%3Daps&field-keywords=tony+buzan&x=0&y=0&ih=6_2_0_0_0_1_1_0_0_1.34_102&fsc=6">Tony Buzan</a>, and learned more from <a href="http://www.jumpassociates.com">Jump</a>, and if you like them you might enjoy <a href="http://www.youtube.com/user/theRSAorg">the RSA talks</a>.</p>
          </div>
        </div>

        <div class="row-fluid">
          <div class="span3">
            <h3>Why a Mindmap?</h3>
          </div>
          <div class="span6">
            <p>We found Mindmaps to be a great way of journalling for lots of reasons. Most people these days take on a travel blog - travel blogs are great as they are real-time sharing and people can comment on it and you can link to it after you're done. In practice though, writing a paper journal meant that rather than sit with a netbook or at an Internet cafe, we could be sitting out on a terrace with a beer or a coffee reflecting on our recent adventures. It is perfect then that we could do it as a couple, jointly reflecting and writing together.</p>

            <p>Because you can add a little at a time it was also not a big undertaking and it was easy to add back into previous pages as you remember other fun things that happened, or later realise what it was you liked most.</p>

            <p>So much of the travel experience is visual. So, while <a href="http://www.flickr.com/photos/palojono">we have photographs too</a>, Mindmapping allowed a quick sketch here or there for whatever was memorable to us - even if we couldn't capture a photo at the time.  Being visual hopefully makes it easier to share with others and easier for people to browse through the pages rather than wading through text. And finally, it's a good skill for us to practice while we travelled. Hopefully you'll see some improvement as the journalling went on.</p>
          </div>
