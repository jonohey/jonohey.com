	        <p>
			  My advisor is Professor <a href="http://www.me.berkeley.edu/faculty/agogino/index.html">Alice Agogino</a> and my research interests stem from the desire to make things that people actually need. In my short career I realize that I have spent a great deal of time on several occasions creating a great solution to the wrong problem. I'm interested in how we can figure out what people actually want, particularly when that's performed within a multidisciplinary team. This is the process of framing the problem that you want to solve; how to decide what it is you should make. (more about <a href="framing/">framing in design and innovation</a>)
			</p>
			<p>
	          I have also studied the role of metaphors in the design process, both as a tool and, more subtly, as they give us an understanding of the design and creative processes themselves.
	        </p>

	        <p>
	          Not so long ago I spent a fantastic year working at <a href="http://www.creax.com">CREAX</a>, one of the most innovative companies in the world, both applying and developing the CREAX Systematic Innovation Methodology (SIM) based on TRIZ the Theory of Inventive Problem Solving.
	        </p>

	        <p>
	          I received a Masters in Innovation and Engineering Design at the <a href="http://www.bath.ac.uk/mech-eng/">University of Bath</a>, England under <a href="http://www.bath.ac.uk/expertise/showperson.php?username=enssjc">Steve Culley</a>. My Masters research was performed during time at the <a href="http://www.inpg.fr">Institut Polytechnique de Grenoble</a> (INPG) in France with the guidance of Jean-Francois Boujut. The title of my research was: Development of a new tool to guide innovation during upstream project phases.
	        </p>
